class Calculator:
    """Performs basic arithmetic operations."""

    def add(self, a: float, b: float) -> float:
        """Doc string."""
        return a + b

    def subtract(self, a: float, b: float) -> float:
        """Doc string."""
        return a - b

    def multiply(self, a: float, b: float) -> float:
        """Doc string."""
        return a * b
